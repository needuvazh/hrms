CREATE INDEX IF NOT EXISTS idx_employee_profiles_tenant_status ON employee.employee_profiles (tenant_id, employee_status, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_employee_addresses_tenant_employee ON employee.employee_addresses (tenant_id, employee_id, is_active, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_employee_emergency_contacts_tenant_employee ON employee.employee_emergency_contacts (tenant_id, employee_id, is_active, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_employee_dependants_tenant_employee ON employee.employee_dependants (tenant_id, employee_id, is_active, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_employee_beneficiaries_tenant_employee ON employee.employee_beneficiaries (tenant_id, employee_id, is_active, priority_order);
CREATE INDEX IF NOT EXISTS idx_employee_documents_tenant_employee ON employee.employee_documents (tenant_id, employee_id, is_active, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_employee_documents_tenant_expiry ON employee.employee_documents (tenant_id, expiry_date, is_active);
CREATE INDEX IF NOT EXISTS idx_employee_history_tenant_employee ON employee.employee_employment_history (tenant_id, employee_id, effective_date DESC);
