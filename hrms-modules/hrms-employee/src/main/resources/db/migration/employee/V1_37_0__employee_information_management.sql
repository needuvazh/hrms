CREATE TABLE IF NOT EXISTS employee.employee_policy_acknowledgements (
    acknowledgement_id UUID PRIMARY KEY,
    tenant_id VARCHAR(64) NOT NULL,
    onboarding_id UUID NOT NULL,
    policy_code VARCHAR(128) NOT NULL,
    policy_version VARCHAR(64) NOT NULL,
    accepted_flag BOOLEAN NOT NULL,
    accepted_date_time TIMESTAMPTZ NOT NULL,
    accepted_by VARCHAR(64),
    remarks VARCHAR(500),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_by VARCHAR(64) NOT NULL,
    CONSTRAINT fk_employee_policy_ack_onboarding FOREIGN KEY (onboarding_id) REFERENCES employee.employee_digital_onboarding(onboarding_id)
