CREATE TABLE IF NOT EXISTS employee.employee_documents (
    employee_document_id UUID PRIMARY KEY,
    tenant_id VARCHAR(64) NOT NULL,
    employee_id UUID NOT NULL,
    document_type VARCHAR(64) NOT NULL,
    document_name VARCHAR(255) NOT NULL,
    document_number VARCHAR(128),
    issuing_country_id UUID,
    issuing_authority VARCHAR(255),
    issue_date DATE,
    expiry_date DATE,
    file_name VARCHAR(255),
    file_path VARCHAR(500),
    file_type VARCHAR(128),
    file_size BIGINT,
    verification_status VARCHAR(32) NOT NULL,
    verified_by VARCHAR(64),
    verified_date TIMESTAMPTZ,
    alert_enabled BOOLEAN NOT NULL DEFAULT FALSE,
    remarks VARCHAR(500),
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_by VARCHAR(64) NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_by VARCHAR(64) NOT NULL,
    CONSTRAINT fk_employee_documents_employee FOREIGN KEY (employee_id) REFERENCES employee.employees(id),
    CONSTRAINT ck_employee_documents_type CHECK (document_type IN (
        'PASSPORT','VISA','RESIDENCE_CARD','LABOUR_CARD','CIVIL_ID','EDUCATIONAL_CERTIFICATE',
        'PROFESSIONAL_LICENSE','INSURANCE_CARD','CONTRACT','OFFER_LETTER','OTHER')),
    CONSTRAINT ck_employee_documents_verification CHECK (verification_status IN ('PENDING','VERIFIED','REJECTED')),
    CONSTRAINT ck_employee_documents_issue_expiry CHECK (expiry_date IS NULL OR issue_date IS NULL OR issue_date <= expiry_date)
);

CREATE TABLE IF NOT EXISTS employee.employee_employment_history (
    employment_history_id UUID PRIMARY KEY,
    tenant_id VARCHAR(64) NOT NULL,
    employee_id UUID NOT NULL,
    action_type VARCHAR(64) NOT NULL,
    old_department_id UUID,
    new_department_id UUID,
    old_designation_id UUID,
    new_designation_id UUID,
    old_location_id UUID,
    new_location_id UUID,
    old_grade_id UUID,
    new_grade_id UUID,
    old_manager_id UUID,
    new_manager_id UUID,
    effective_date DATE NOT NULL,
    remarks VARCHAR(500),
    supporting_document_path VARCHAR(500),
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_by VARCHAR(64) NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_by VARCHAR(64) NOT NULL,
    CONSTRAINT fk_employee_employment_history_employee FOREIGN KEY (employee_id) REFERENCES employee.employees(id),
    CONSTRAINT ck_employee_employment_history_action CHECK (action_type IN (
        'JOINING','PROMOTION','TRANSFER','SECONDMENT','DESIGNATION_CHANGE','DEPARTMENT_CHANGE',
        'LOCATION_CHANGE','GRADE_CHANGE','REPORTING_MANAGER_CHANGE'))
);

