CREATE SEQUENCE IF NOT EXISTS employee.employee_code_seq START WITH 1000 INCREMENT BY 1;

CREATE TABLE IF NOT EXISTS employee.employee_profiles (
    employee_id UUID PRIMARY KEY,
    tenant_id VARCHAR(64) NOT NULL,
    employee_code VARCHAR(64) NOT NULL,
    first_name VARCHAR(128) NOT NULL,
    middle_name VARCHAR(128),
    last_name VARCHAR(128) NOT NULL,
    full_name VARCHAR(300),
    arabic_name VARCHAR(300),
    profile_photo_path VARCHAR(500),
    gender_id UUID,
    date_of_birth DATE,
    marital_status_id UUID,
    nationality_id UUID,
    blood_group VARCHAR(16),
    personal_email VARCHAR(255),
    official_email VARCHAR(255),
    primary_mobile_number VARCHAR(32),
    secondary_mobile_number VARCHAR(32),
    alternate_contact_number VARCHAR(32),
    employee_status VARCHAR(32) NOT NULL,
    date_of_joining DATE,
    confirmation_date DATE,
    probation_end_date DATE,
    retirement_date DATE,
    workforce_category VARCHAR(32) NOT NULL,
    department_id UUID,
    designation_id UUID,
    job_title VARCHAR(128),
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_by VARCHAR(64) NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_by VARCHAR(64) NOT NULL,
    CONSTRAINT fk_employee_profiles_employee FOREIGN KEY (employee_id) REFERENCES employee.employees(id),
    CONSTRAINT uq_employee_profiles_tenant_code UNIQUE (tenant_id, employee_code),
    CONSTRAINT uq_employee_profiles_tenant_official_email UNIQUE (tenant_id, official_email),
    CONSTRAINT ck_employee_profiles_status CHECK (employee_status IN ('ACTIVE','INACTIVE','PROBATION','SUSPENDED','TERMINATED')),
    CONSTRAINT ck_employee_profiles_workforce CHECK (workforce_category IN ('OMANI_NATIONAL','EXPATRIATE')),
    CONSTRAINT ck_employee_profiles_confirmation_date CHECK (confirmation_date IS NULL OR date_of_joining IS NULL OR confirmation_date >= date_of_joining),
    CONSTRAINT ck_employee_profiles_probation_end_date CHECK (probation_end_date IS NULL OR date_of_joining IS NULL OR probation_end_date >= date_of_joining)
);

